    <h3 id="ingredients">Ingredients</h3>
    <ul>
    <li>250g butter</li>
    <li>375g caster sugar</li>
    <li>3 eggs</li>
    <li>3tsp vanilla extract</li>
    <li>335g chocolate</li>
    <li>140g plain flour</li>
    <li>45g cocoa powder</li>
    <li>1.5 tsp salt</li>
    </ul>
    <h3 id="directions">Directions</h3>
    <ol>
    <li>Preheat oven to 180Â°C &amp; line baking tray</li>
    <li>Melt 1/2 of chocolate and allow to cool</li>
    <li>Cream butter and sugar in stand mixer</li>
    <li>Beat in eggs &amp; vanilla until fluffy</li>
    <li>Mix in the chocolate until combined</li>
    <li>Sift in flour &amp; cocoa powder &amp; fold in</li>
    <li>Fold in the chocolate chunks</li>
    <li>Bake for 20 mins and check centre. If very gloopy leave for 5-7 more mins before removing</li>
    <li>Cool completely and slice</li>
    </ol>    
