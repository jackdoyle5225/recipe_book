    <p><img src="../images/path to image" alt="Lemon Cheesecake"></p>
    <h3 id="ingredients">Ingredients</h3>
    <ul>
    <li>100g unsalted butter</li>
    <li>300g digestive biscuits</li>
    <li>600g cream cheese</li>
    <li>300ml double cream</li>
    <li>100g icing sugar</li>
    <li>juice &amp; zest of 2 large lemons</li>
    </ul>
    <h3 id="directions">Directions</h3>
    <ol>
    <li>Crush biscuits using freezer bag and a rolling pin</li>
    <li>Melt the butter and combine with the biscuits in a bowl</li>
    <li>Press the mixture into the bottom of a large springform tin</li>
    <li>Place the springform tin into the fridge to chill while we prepare the cheesecake</li>
    <li>Whisk together the cream cheese and the icing sugar in a stand mixer</li>
    <li>Add in the double cream and whisk</li>
    <li>As the mixture starts to thicken, add in the lemon juice and 3/4 of the zest</li>
    <li>Continue to whisk and fold with a spatula until the mixture is very thick</li>
    <li>Once whisked, spread over the biscuit base</li>
    <li>Decorate with the remaining zest and chill for 5-6 hours in the fridge</li>
    </ol>
