    <h1 id="neopolitan-pizza">Neopolitan Pizza</h1>
    <p><img src="../images/pizza.jpg" width="600" height="600" alt="Pizza"></p>
    <h3 id="ingredients">Ingredients</h3>
    <ul>
    <li>570g all-purpose flour</li>
    <li>7g instant yeast</li>
    <li>370g water</li>
    <li>11g salt</li>
    <li>2 balls of buffalo mozzarella</li>
    <li>sauce of your choice</li>
    <li>toppings of your choice</li>
    <li>fresh basil (not optional)</li>
    </ul>
    <p>makes 4 decent sized pizzas</p>
    <h3 id="directions">Directions</h3>
    <ol>
    <li>Mix the flour and yeast together in a large mixing bowl</li>
    <li>Add the water and mix to combine in a stand mixer</li>
    <li>Add the salt and knead however you know how until the dough passes the window-pane test</li>
    <li>Form the dough into a taut ball, place in an oiled bowl &amp; cover with clingfilm</li>
    <li>Place dough in the fridge for 48 hours, or alternatively leave it at room temperature for 2 hours.</li>
    <li>Split dough into 4 even pieces, form them into balls and place them on a floured covered baking sheet &amp; let them rest for 2 hours</li>
    <li>Place pizza stone on barbequeue and preheat to as high as it will go for 10 / 15 mins</li>
    <li>Form dough into desired shape and add sauce &amp; toppings</li>
    <li>Transfer pizza to stone and cook for 6-7 mins on the barbequeue until base is crispy but not burnt</li>
    <li>Remove from oven and add fresh basil</li>
    </ol>
